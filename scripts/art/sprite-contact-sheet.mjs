import { readFileSync } from "node:fs";
import { mkdir, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { PNG } from "pngjs";
import { encodePng, hex } from "./write-png.mjs";

const CELL = 48;
const GUTTER = 4;
const SCALE_4X = 4;
const PANEL_BG = "#262019";

function layoutGrid(count) {
  const columns = Math.max(1, Math.ceil(Math.sqrt(count)));
  const rows = Math.max(1, Math.ceil(count / columns));
  return { columns, rows };
}

/** Builds deterministic native-scale and 4x diagnostic review sheets in registry order. */
export function buildSpriteContactSheets(spritesDir, registry) {
  const entries = registry.map(({ name }) => ({
    name,
    png: PNG.sync.read(readFileSync(resolve(spritesDir, `${name}.png`))),
  }));
  const { columns, rows } = layoutGrid(entries.length);
  const width = columns * CELL + (columns - 1) * GUTTER;
  const height = rows * CELL + (rows - 1) * GUTTER;
  const [bgR, bgG, bgB] = hex(PANEL_BG);
  const pixel = (x, y) => {
    const stride = CELL + GUTTER;
    const column = Math.floor(x / stride);
    const row = Math.floor(y / stride);
    const localX = x - column * stride;
    const localY = y - row * stride;
    const entry = entries[row * columns + column];
    if (!entry || localX >= CELL || localY >= CELL) return [bgR, bgG, bgB, 255];

    const offsetX = Math.floor((CELL - entry.png.width) / 2);
    const offsetY = CELL - entry.png.height;
    const sourceX = localX - offsetX;
    const sourceY = localY - offsetY;
    if (sourceX < 0 || sourceY < 0 || sourceX >= entry.png.width || sourceY >= entry.png.height) {
      return [bgR, bgG, bgB, 255];
    }
    const at = (sourceY * entry.png.width + sourceX) * 4;
    const alpha = entry.png.data[at + 3] / 255;
    return [
      Math.round(entry.png.data[at] * alpha + bgR * (1 - alpha)),
      Math.round(entry.png.data[at + 1] * alpha + bgG * (1 - alpha)),
      Math.round(entry.png.data[at + 2] * alpha + bgB * (1 - alpha)),
      255,
    ];
  };
  const oneX = encodePng(width, height, pixel);
  const fourX = encodePng(width * SCALE_4X, height * SCALE_4X, (x, y) =>
    pixel(Math.floor(x / SCALE_4X), Math.floor(y / SCALE_4X)),
  );
  return { oneX, fourX };
}

/** Writes the committed sprite review artifacts generated by `npm run art`. */
export async function writeSpriteContactSheets(spritesDir, docsDir, registry) {
  const { oneX, fourX } = buildSpriteContactSheets(spritesDir, registry);
  await mkdir(docsDir, { recursive: true });
  await writeFile(resolve(docsDir, "sprite-sheet-1x.png"), oneX);
  await writeFile(resolve(docsDir, "sprite-sheet-4x.png"), fourX);
}
